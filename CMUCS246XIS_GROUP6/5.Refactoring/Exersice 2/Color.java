package Packages;
public class Color {

    private byte red;
    private byte blue;
    private byte green;

    public Color(byte red, byte blue, byte green) {
        this.red = red;
        this.blue = blue;
        this.green = green;
    }

    public byte getRed() {
        return red;
    }

    public byte getBlue() {
        return blue;
    }

    public byte getGreen() {
        return green;
    }
}
